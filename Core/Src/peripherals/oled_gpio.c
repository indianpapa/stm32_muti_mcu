/*OLED底层驱动
*@采用列行式，分辨率为128*64
*存放格式如下.
*[0]0 1 2 3 ... 127
*[1]0 1 2 3 ... 127
*[2]0 1 2 3 ... 127
*[3]0 1 2 3 ... 127
*[4]0 1 2 3 ... 127
*[5]0 1 2 3 ... 127
*[6]0 1 2 3 ... 127
*[7]0 1 2 3 ... 127 */

#define X_WIDTH 128
#define Y_WIDTH 64

#define	BRIGHTNESS	0xcf

extern const char _F6x8[];
extern const char _F16x16_Idx[];
extern const char _F16x16[];
extern const char _F8x16[];

static void _oled_gpio_WrCmd(_oled_gpio_device_internal_t* device, uint_8 cmd) {
	uint_8 i = 8;

	device->dc_control(GPIO_PIN_RESET);
	device->clk_control(GPIO_PIN_RESET);

	while (i--) {
		if (cmd & 0x80) {
			device->mosi_control(GPIO_PIN_SET);
		} else {
			device->mosi_control(GPIO_PIN_RESET);
		}

		device->clk_control(GPIO_PIN_SET);
		device->clk_control(GPIO_PIN_RESET);

		cmd <<= 1;
	}
}

static void _oled_gpio_WrDat(_oled_gpio_device_internal_t* device, uint_8 data) {
	uint_8 i = 8;
	device->dc_control(GPIO_PIN_SET);
	device->clk_control(GPIO_PIN_RESET);
	while (i--) {
		if (data & 0x80) {
			device->mosi_control(GPIO_PIN_SET);
		} else {
			device->mosi_control(GPIO_PIN_RESET);
		}

		device->clk_control(GPIO_PIN_SET);
		device->clk_control(GPIO_PIN_RESET);

		data <<= 1;
	}
}

static void _oled_gpio_Fill(_oled_gpio_device_internal_t* device, uint_8 bmp_data) {
	uint_8 y, x;

	for (y = 0; y < 8; y++) {
		_oled_gpio_WrCmd(device, 0xb0 + y);
		_oled_gpio_WrCmd(device, 0x01);
		_oled_gpio_WrCmd(device, 0x10);
		for (x = 0; x < X_WIDTH; x++) {
			_oled_gpio_WrDat(device, bmp_data);
		}
	}
}

/*设置起始地址
 *Y轴是按8格递进的，y轴0~63，只能按8格的倍数显示，
 *因为列行式只能按8个字节进行 */
static void _oled_gpio_Set_Pos(_oled_gpio_device_internal_t* device, uint_8 x, uint_8 y) {
	_oled_gpio_WrCmd(device, 0xb0 + (y >> 3));
	_oled_gpio_WrCmd(device, ((x & 0xf0) >> 4) | 0x10);
	_oled_gpio_WrCmd(device, (x & 0x0f) | 0x01);
}

_oled_gpio_device_internal_t* current_device;

static void oled_gpio_device_init_delay(uint_16 type, void* pv) {
	current_device->rst_control(GPIO_PIN_SET);
	_oled_gpio_WrCmd(current_device, 0xae); //--turn off oled panel
	_oled_gpio_WrCmd(current_device, 0x00); //---set low column address
	_oled_gpio_WrCmd(current_device, 0x10); //---set high column address
	_oled_gpio_WrCmd(current_device, 0x40); //--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
	_oled_gpio_WrCmd(current_device, 0x81); //--set contrast control register
	_oled_gpio_WrCmd(current_device, BRIGHTNESS); // Set SEG Output Current Brightness
	_oled_gpio_WrCmd(current_device, 0xa1); //--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
	_oled_gpio_WrCmd(current_device, 0xc8); //Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
	_oled_gpio_WrCmd(current_device, 0xa6); //--set normal display
	_oled_gpio_WrCmd(current_device, 0xa8); //--set multiplex ratio(1 to 64)
	_oled_gpio_WrCmd(current_device, 0x3f); //--1/64 duty
	_oled_gpio_WrCmd(current_device, 0xd3); //-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
	_oled_gpio_WrCmd(current_device, 0x00); //-not offset
	_oled_gpio_WrCmd(current_device, 0xd5); //--set display clock divide ratio/oscillator frequency
	_oled_gpio_WrCmd(current_device, 0x80); //--set divide ratio, Set Clock as 100 Frames/Sec
	_oled_gpio_WrCmd(current_device, 0xd9); //--set pre-charge period
	_oled_gpio_WrCmd(current_device, 0xf1); //Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	_oled_gpio_WrCmd(current_device, 0xda); //--set com pins hardware configuration
	_oled_gpio_WrCmd(current_device, 0x12);
	_oled_gpio_WrCmd(current_device, 0xdb); //--set vcomh
	_oled_gpio_WrCmd(current_device, 0x40); //Set VCOM Deselect Level
	_oled_gpio_WrCmd(current_device, 0x20); //-Set Page Addressing Mode (0x00/0x01/0x02)
	_oled_gpio_WrCmd(current_device, 0x02); //
	_oled_gpio_WrCmd(current_device, 0x8d); //--set Charge Pump enable/disable
	_oled_gpio_WrCmd(current_device, 0x14); //--set(0x10) disable
	_oled_gpio_WrCmd(current_device, 0xa4); // Disable Entire Display On (0xa4/0xa5)
	_oled_gpio_WrCmd(current_device, 0xa6); // Disable Inverse Display On (0xa6/a7)
	_oled_gpio_WrCmd(current_device, 0xaf); //--turn on oled panel
	_oled_gpio_Fill(current_device, 0x00);  //初始清屏
	_oled_gpio_Set_Pos(current_device, 0, 0);
}

bool_t oled_gpio_device_init(_oled_gpio_device_internal_t* device) {
	if (!device->init_gpio()) return false;

	device->clk_control(GPIO_PIN_SET);
	device->rst_control(GPIO_PIN_RESET);

	current_device = device;

	callback_scheduler_postdely_ms(oled_gpio_device_init_delay, device, 777, 50);

	return true;
}

void oled_print6x8(_oled_gpio_device_internal_t* device, uint_8 x, uint_8 y, char* ch, const char* F6x8) {
	uint_8 c = 0, i = 0, j = 0;

	while (*(ch + j) != '\0') {
	    c = *(ch+j) - 32;
		if (x > 126) {
		    x = 0;
			y++;
		}

		_oled_gpio_Set_Pos(device, x, y);
		for (i = 0; i < 6; i++) {
			_oled_gpio_WrDat(device, *(_F6x8 + c * 6 + i));
		}
		x += 6;
		j++;
	}
}

void oled_print8x16(_oled_gpio_device_internal_t* device, uint_8 x, uint_8 y, char* ch, const char* F8x16) {
	uint_8 c = 0, i = 0, j = 0;

	while (*(ch + j) != '\0') {
		c = *(ch + j) - 32;
	    if (x > 120) {
			x = 0;
			y++;
		}

	    _oled_gpio_Set_Pos(device, x, y);
	  	for (i = 0; i < 8; i++) {
	  		_oled_gpio_WrDat(device, *(_F8x16 + c * 16 + i));
	  	}

	  	_oled_gpio_Set_Pos(device, x, y + 8);
	  	for (i = 0; i < 8; i++) {
	  		_oled_gpio_WrDat(device, *(_F8x16 + c * 16 + i + 8));
	  	}

	  	x += 8;
	  	j++;
	}
}

void oled_print16x16(_oled_gpio_device_internal_t* device, uint_8 x, uint_8 y, char* ch, const char* F16x16_Idx, const char* F16x16) {
	uint_8 wm=0, ii = 0;
	uint_16 adder=1;

	while (*(ch + ii) != '\0') {
	    wm = 0;
	    adder = 1;
	    while (*(F16x16_Idx + wm) > 127) {
		    if (*(F16x16_Idx + wm) == *(ch+ii)) {
			    if (*(F16x16_Idx+wm + 1) == *(ch+ii + 1)) {
				    adder = wm * 16;
				    break;
			    }
		    }
		    wm += 2;
	    }

	    if (x > 118) {
	    	x = 0;
	    	y++;
	    }

	    _oled_gpio_Set_Pos(device, x, y);
	    if (adder != 1) {// 显示汉字
	    	_oled_gpio_Set_Pos(device, x , y);
		    for (wm = 0; wm < 16; wm++) {
		    	_oled_gpio_WrDat(device, *(_F16x16 + adder));
			    adder += 1;
		    }

		    _oled_gpio_Set_Pos(device, x, y + 8);
		    for (wm = 0; wm < 16; wm++) {
			    _oled_gpio_WrDat(device, *(_F16x16 + adder));
			    adder += 1;
		    }
	    } else {		  //显示空白字符
		    ii += 1;
		    _oled_gpio_Set_Pos(device, x,y);
		    for (wm = 0; wm < 16; wm++) {
		    	_oled_gpio_WrDat(device, 0);
		    }

		    _oled_gpio_Set_Pos(device, x,y + 1);
		    for(wm = 0;wm < 16;wm++) {
		    	_oled_gpio_WrDat(device, 0);
		    }
	    }
	    x += 16;
	    ii += 2;
	}
}

void oled_print(_oled_gpio_device_internal_t* device, uint_8 x, uint_8 y, char *ch, uint_8 char_size, uint_8 ascii_size) {
	char ch2[3];
	uint_8 ii=0;

	while(*(ch+ii) != '\0') {
		if (*(ch+ii) > 127) {       //大于127为中文，小于等于127为ASCII
		    ch2[0] = *(ch+ii);
			ch2[1] = *(ch+ii+1);
			ch2[2] = '\0';			//汉字为两个字节
			oled_print16x16(device, x , y, ch2, _F16x16_Idx, _F16x16);	//显示汉字
			x += 16;
			ii += 2;
		} else {
			ch2[0] = *(ch+ii);
			ch2[1] = '\0';			//字母占一个字节
			if(TYPE8X16 == ascii_size) {
				oled_print8x16(device, x, y, ch2, _F8x16);	//显示字母
				x += 8;
			} else if (TYPE6X8 == ascii_size) {
				oled_print6x8(device, x, y, ch2, _F6x8);	//显示字母
				x += 6;
			}

			ii+= 1;
		}
	}
}

void oled_put_pixel(_oled_gpio_device_internal_t* device, uint_8 x, uint_8 y) {
	uint_8 data;

	data = 0x01 << (y % 8);
	_oled_gpio_WrCmd(device, 0xb0 + (y >> 3));
	_oled_gpio_WrCmd(device, ((x&0xf0) >> 4) | 0x10);
	_oled_gpio_WrCmd(device, (x&0x0f) | 0x00);
	_oled_gpio_WrDat(device, data);
}

void oled_clear(_oled_gpio_device_internal_t* device) {
	uint_8 y, x;

	for (y = 0; y < 8; y++) {
		_oled_gpio_WrCmd(device, 0xb0 + y);
		_oled_gpio_WrCmd(device, 0x01);
		_oled_gpio_WrCmd(device, 0x10);
		for (x = 0; x < X_WIDTH; x++) {
			_oled_gpio_WrDat(device, 0);
		}
	}
}

void oled_clear_y(_oled_gpio_device_internal_t* device, uint_8 y) {
	uint_8 x;

	_oled_gpio_WrCmd(device, 0xb0 + (y>>3));
	_oled_gpio_WrCmd(device, 0x01);
	_oled_gpio_WrCmd(device, 0x10);
	for (x = 0; x < X_WIDTH; x++) {
		_oled_gpio_WrDat(device, 0);
	}
}

void oled_clear_line_area(_oled_gpio_device_internal_t* device, uint_8 start_x, uint_8 start_y, uint_8 width) {
	uint_8 x;

	_oled_gpio_WrCmd(device, 0xb0 + (start_y >> 3));
	_oled_gpio_WrCmd(device, 0x01);
	_oled_gpio_WrCmd(device, 0x10);
	for (x = start_x; x < width; x++) {
		_oled_gpio_WrDat(device, 0);
	}
}

